def soma(num1, num2):
    resultado = num1 + num2
    return resultado

def subtracao(num1, num2):
    resultado = num1 - num2
    return resultado

def multiplicacao(num1, num2):
    resultado = num1 * num2
    return resultado

def divisao(num1, num2):
    resultado = num1 / num2
    return resultado

def potencializacao(num1, num2):
    resultado = num1 ** num2
    return resultado

def porcentagem(num1, num2):
    resultado = num1 * (num2 / 100)
    return resultado
